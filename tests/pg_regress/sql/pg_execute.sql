CREATE OR REPLACE FUNCTION ts_pg_execute_invalid_type_ref() RETURNS bool
LANGUAGE typescript AS $$
  try {
    _pg.execute(
      "SELECT $1::int4 AS value",
      { type: 23, value: 1 },
    );
    return false;
  } catch (err) {
    return String(err.message ?? err) ===
      "_pg.execute typed parameters require a string type name";
  }
$$;

